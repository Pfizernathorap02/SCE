package com.pfizer.sce.beans;

public class GuestTrainerEvent {
 private String mapId;
 private String repEmail;
 private String eventName;
 private String repIsAccepted;
 private String repEmailIsSent;
public String getMapId() {
	return mapId;
}
public void setMapId(String mapId) {
	this.mapId = mapId;
}
public String getRepEmail() {
	return repEmail;
}
public void setRepEmail(String repEmail) {
	this.repEmail = repEmail;
}
public String getEventName() {
	return eventName;
}
public void setEventName(String eventName) {
	this.eventName = eventName;
}
public String getRepIsAccepted() {
	return repIsAccepted;
}
public void setRepIsAccepted(String repIsAccepted) {
	this.repIsAccepted = repIsAccepted;
}
public String getRepEmailIsSent() {
	return repEmailIsSent;
}
public void setRepEmailIsSent(String repEmailIsSent) {
	this.repEmailIsSent = repEmailIsSent;
}
 
 
}
