package com.pfizer.sce.beans;

public class EventCourse {

	public Integer eventCourseId;
	public Integer eventId;
	public String courseName;
	public Integer templateId;
	public String tableName;
	public Integer getEventCourseId() {
		return eventCourseId;
	}
	public void setEventCourseId(Integer eventCourseId) {
		this.eventCourseId = eventCourseId;
	}
	public Integer getEventId() {
		return eventId;
	}
	public void setEventId(Integer eventId) {
		this.eventId = eventId;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public Integer getTemplateId() {
		return templateId;
	}
	public void setTemplateId(Integer templateId) {
		this.templateId = templateId;
	}
	public String getTableName() {
		return tableName;
	}
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	
	
	
	
	
}
